package com.sunkaisens.omc.util.hss;

public class EquipInfo {

	private String equip_resource;
	private String equip_ipaddr;
	private String status;

	public String getEquip_resource() {
		return equip_resource;
	}

	public void setEquip_resource(String equip_resource) {
		this.equip_resource = equip_resource;
	}

	public String getEquip_ipaddr() {
		return equip_ipaddr;
	}

	public void setEquip_ipaddr(String equip_ipaddr) {
		this.equip_ipaddr = equip_ipaddr;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
